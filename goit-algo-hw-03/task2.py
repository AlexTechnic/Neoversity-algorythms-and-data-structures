""" Программа містить функцію для малювання фракталу 'сніжинки Коха'. 
    Сніжинка Коха складається з трьох кривих Коха, які утворюють рівносторонній трикутник.
    Кожна крива Коха складається з чотирьох відрізків, які утворюють кут 60 градусів.
    Кожен відрізок замінюється на чотири відрізки, довжина яких дорівнює третині відповідного відрізка.
    Функція draw_snowflake приймає параметр order, який визначає рівень рекурсії.""" 

import turtle  # Імпорт модуля turtle для малювання графіки на екрані

def koch_curve(t, order, size):
    """Малює один сегмент кривої Коха."""
    if order == 0:
        t.forward(size)
    else:
        for angle in [60, -120, 60, 0]:
            koch_curve(t, order - 1, size / 3)
            t.left(angle)

def draw_snowflake(order, size=300):
    """Малює сніжинку Коха з трьох кривих."""
    window = turtle.Screen()
    window.bgcolor("white")

    t = turtle.Turtle()
    t.speed(0)  
    t.penup()
    t.goto(-size / 2, size / 3)
    t.pendown()

    for _ in range(3):
        koch_curve(t, order, size)
        t.right(120)

    window.mainloop()

if __name__ == "__main__":
    # Запит рівня рекурсії у користувача
    order = int(input("Введіть рівень рекурсії для сніжинки Коха: "))

    # Виклик функції для малювання сніжинки
    draw_snowflake(order)

